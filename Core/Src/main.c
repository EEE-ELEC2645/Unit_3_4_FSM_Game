// ===== UPDATE & RENDER FUNCTIONS =====

/**
 * @brief Update character logic (FSM, physics, input handling)
 * 
 * This function handles all game logic updates:
 * - Processes input (joystick, jump button)
 * - Updates character FSM state transitions
 * - Applies physics (gravity, velocity, collisions)
 * - Updates animation frames
 * 
 * Separated from rendering for cleaner code architecture.
 */
void update_character(Joystick_t* joy) {
    // Update character FSM with current input
    // Pass jump_button_pressed flag, then clear it for next frame
    Character_Update(&game_character, joy, jump_button_pressed);
    jump_button_pressed = 0;  // Clear jump button flag
}

/**
 * @brief Render the game to the LCD screen
 * 
 * This function handles all rendering/drawing:
 * - Clears the screen buffer
 * - Draws environment (ground, platforms)
 * - Draws character sprite
 * - Draws debug information (state, position, velocity)
 * - Refreshes LCD to display the frame
 * 
 * Separated from game logic for cleaner code architecture.
 */
void render_game(void) {
    // Clear screen buffer
    LCD_Fill_Buffer(0);
    
    // Draw environment
    Character_DrawGround();
    
    // Draw character at current position with animation
    Character_Draw(&game_character);
    
    // Draw debug info
    LCD_printString("State:", 180, 10, 1, 2);
    LCD_printString((char*)Character_GetStateName(&game_character), 180, 30, 1, 2);
    
    char y_str[16];
    sprintf(y_str, "Y:%d", game_character.y);
    LCD_printString(y_str, 180, 50, 1, 2);
    
    char v_str[16];
    sprintf(v_str, "VY:%.1f", game_character.velocity_y);
    LCD_printString(v_str, 180, 70, 1, 2);
    
    // Refresh LCD to display this frame
    LCD_Refresh(&cfg0);
}

// ===== Interrupt Callback =====

/**
  * @brief EXTI line detection callback - Jump Button
  * @param GPIO_Pin: Specifies which GPIO pin triggered the interrupt
  * 
  * @note BTN2 is used for the jump action. When pressed, it sets the jump flag
  *       which is read by the character FSM in the main loop.
  *       
  *       IMPORTANT: We keep ISRs SHORT and SIMPLE - just set flags.
  *       The main loop handles everything else.
  *       Never use HAL_Delay() in an interrupt handler as it can cause the system to hang!
  */
void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
  static uint32_t btn2_last_interrupt_time = 0;
  uint32_t current_time = HAL_GetTick();
  
  // Check if jump button (BTN2) was pressed
  if (GPIO_Pin == BTN2_Pin)
  {
    // Debouncing: ignore interrupts that happen too quickly (within 100ms)
    if ((current_time - btn2_last_interrupt_time) > JUMP_DEBOUNCE_DELAY)
    {
      btn2_last_interrupt_time = current_time;
      
      // Set jump flag - will be read by character FSM in main loop
      jump_button_pressed = 1;
    }
  }
}

